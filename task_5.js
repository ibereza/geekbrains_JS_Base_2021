/*
Реализовать основные 4 арифметические операции в виде функций с двумя параметрами.
Обязательно использовать оператор return.
*/

function sum(a, b) {
    return a + b;
}

function sub(a, b) {
    return a - b;
}

function mul(a, b) {
    return a * b;
}

function dev(a, b) {
    return a / b;
}

console.log(sum(2, -5));
console.log(sub(2, -5));
console.log(mul(2, -5));
console.log(dev(2, -5));